class Operators {
    public static void main(String args[]){
        // logical operators 
        boolean a=false;
        boolean b=true;
        System.out.println(a&&b); // false

        // bit wise 
        int c=9;
        int d=5;
        System.out.println(c&d);
        System.out.println(c|d);
        System.out.println(c^d);
        System.out.println(~c);


    }
}